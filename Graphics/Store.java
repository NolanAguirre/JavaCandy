public class Store{
    private int x;
    private int y;
    public Store(){
        x = 0;
        y = 0;
    }
    public int getX(){
        return x;
    }
    public void setX(int foo){
        x = foo;
    }
    public int getY(){
        return y;
    }
    public void setY(int foo){
        y = foo;
    }
}
